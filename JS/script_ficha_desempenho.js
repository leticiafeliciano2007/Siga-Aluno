// Função para exibir as atividades de acordo com o intermediário selecionado
function exibirAtividades(intermediario) {
    const fichaBody = document.getElementById('ficha-body');
    fichaBody.innerHTML = ''; // Limpa o corpo da tabela

    const atividades = atividadesPorIntermediario[intermediario];

    if (atividades) {
        atividades.forEach(item => {
            // Adiciona uma linha para a matéria
            const materiaRow = document.createElement('tr');
            materiaRow.innerHTML = `<td colspan="3"><strong>${item.materia}</strong></td>`;
            fichaBody.appendChild(materiaRow);

            item.atividades.forEach((atividade, index) => {
                const row = document.createElement('tr');
                row.innerHTML = `
                    <td>${atividade.nome}</td>
                    <td>${atividade.mencao}</td>
                    <td>${atividade.descricao}</td>
                `;

                // Alterna as cores das linhas
                if (index % 2 === 0) {
                    row.classList.add('linha-atividade'); // Verde claro
                } else {
                    row.classList.add('linha-atividade-2'); // Laranja claro
                }

                fichaBody.appendChild(row);
            });

            // Linha vazia após cada matéria
            const emptyRow = document.createElement('tr');
            emptyRow.innerHTML = `<td colspan="5"></td>`;
            fichaBody.appendChild(emptyRow);
        });
    }
}

// Evento para mudar as atividades quando o usuário seleciona um intermediário
document.getElementById('FICHA_ALUNO').addEventListener('change', function () {
    exibirAtividades(this.value);
});

